/** Helper: get comments for a specific post */
export function getCommentsByPost(postId: string): Comment[] {
  return mockComments.filter((c) => c.postId === postId);
}

/** Helper: get replies to a specific comment */
export function getReplies(commentId: string): Comment[] {
  return mockComments.filter((c) => c.parentId === commentId);
}

/** Helper: get top-level comments (no parentId) for a post */
export function getTopLevelComments(postId: string): Comment[] {
  return mockComments.filter((c) => c.postId === postId && !c.parentId);
}
